const PIECE_TABLES: Record<string, number[][]> = {
  p: PAWN_TABLE,
  n: KNIGHT_TABLE,
  b: BISHOP_TABLE,
  r: ROOK_TABLE,
  q: QUEEN_TABLE,
  k: KING_MIDDLE_TABLE,
};

function getPieceTableValue(
  piece: string,
  color: string,
  row: number,
  col: number
): number {
  const table = PIECE_TABLES[piece];
  if (!table) return 0;
  const r = color === "w" ? 7 - row : row;
  return table[r][col];
}

function squareToRowCol(square: string): { row: number; col: number } {
  const col = square.charCodeAt(0) - "a".charCodeAt(0);
  const row = 8 - parseInt(square[1]);
  return { row, col };
}

function evaluateBoard(chess: Chess): number {
  if (chess.isCheckmate()) {
    return chess.turn() === "w" ? -99999 : 99999;
  }
  if (chess.isDraw() || chess.isStalemate()) {
    return 0;
  }

  let score = 0;
  const board = chess.board();

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (!piece) continue;
      const value = PIECE_VALUES[piece.type] ?? 0;
      const posValue = getPieceTableValue(piece.type, piece.color, row, col);
      const total = value + posValue;
      score += piece.color === "w" ? total : -total;
    }
  }

  return score;
}

function minimax(
  chess: Chess,
  depth: number,
  alpha: number,
  beta: number,
  isMaximizing: boolean
): number {
  if (depth === 0 || chess.isGameOver()) {
    return evaluateBoard(chess);
  }

  const moves = chess.moves({ verbose: true });

  if (isMaximizing) {
    let best = -Infinity;
    for (const move of moves) {
      chess.move(move);
      const score = minimax(chess, depth - 1, alpha, beta, false);
      chess.undo();
      best = Math.max(best, score);
      alpha = Math.max(alpha, best);
      if (beta <= alpha) break;
    }
    return best;
  } else {
    let best = Infinity;
    for (const move of moves) {
      chess.move(move);
      const score = minimax(chess, depth - 1, alpha, beta, true);
      chess.undo();
      best = Math.min(best, score);
      beta = Math.min(beta, best);
      if (beta <= alpha) break;
    }
    return best;
  }
}

export function getBestMove(fen: string, aiColor: "w" | "b", depth = 3): Move | null {
  const chess = new Chess(fen);

  if (chess.isGameOver()) return null;
  if (chess.turn() !== aiColor) return null;

  const moves = chess.moves({ verbose: true });
  if (moves.length === 0) return null;

  const isMaximizing = aiColor === "w";
  let bestMove: Move | null = null;
  let bestScore = isMaximizing ? -Infinity : Infinity;

  for (const move of moves) {
    chess.move(move);
    const score = minimax(chess, depth - 1, -Infinity, Infinity, !isMaximizing);
    chess.undo();

    if (isMaximizing ? score > bestScore : score < bestScore) {
      bestScore = score;
      bestMove = move;
    }
  }

  return bestMove;
}
